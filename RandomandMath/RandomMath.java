import java.util.*;

public class RandomMath {
    public static void main(String[] args) {
        System.out.println(Math.abs(-99));
        System.out.println(Math.ceil(99.6));
        System.out.println(Math.floor(99.9));
        System.out.println(Math.round(54.2));
        System.out.println(Math.max(56, 51));
        System.out.println(Math.min(23, 20));
        System.out.println(Math.pow(2, 2));
        System.out.println(Math.sqrt(16));
        System.out.println(Math.random());
        System.out.println(Math.exp(16));
        System.out.println(Math.log(8));
        System.out.println(Math.sin(90));
        System.out.println(Math.cos(90));
        int n = 56;
        System.out.println(Integer.toString(n));

    }
}
